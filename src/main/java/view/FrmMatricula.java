  private void txt_alumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_alumnoActionPerformed
    Map<String, String> mapaNombresDni = alumnoController.obtenerMapaNombresDni();
    String nombreSeleccionado = txt_alumno.getText();
    String dni = mapaNombresDni.get(nombreSeleccionado);
    txt_dni.setText(dni != null ? dni : "");
  }//GEN-LAST:event_txt_alumnoActionPerformed

  private void txt_modulosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_modulosActionPerformed
    //moduloController
    Map<String, Integer> mapaNombresId = moduloController.obtenerMapaNombresId();
    String moduloSeleccionado = txt_modulos.getText();
    int dni = mapaNombresId.get(moduloSeleccionado);
    txt_moduloId.setText(dni > 0 ? dni+"" : "");
  }//GEN-LAST:event_txt_modulosActionPerformed

  private void txt_semestreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_semestreActionPerformed
    Map<String, String> mapaDetalleId = semestreController.convertir_a_mapa();
    String detalleSeleccionado = txt_semestre.getText();
    String id = mapaDetalleId.get(detalleSeleccionado);
    txt_semestreId.setText(id  != null ? id  : "");
  }//GEN-LAST:event_txt_semestreActionPerformed

