  public void listarBaseDeDatos(){
    List<String[]> listaAlumnoMatriculado = matriculaController.listarAlumnosMatricula();
    for(int i = 0; i<listaAlumnoMatriculado.size(); i++){
      String[] lista = listaAlumnoMatriculado.get(i);
      Object[] data = {lista[0], lista[1], lista[2], lista[3]};
      this.modelo.addRow(data);
    }
  }
  
  public void limpiarTabla(){
    int fila = this.modelo.getRowCount();
    for(int i = fila-1; i>=0; i++){
      modelo.removeRow(i);
    }
  }
  
  private void configurarAutoCompletar() {
      List<String> nombresCompletos = alumnoController.listarNombresYApellidosAlumnos();
      /*
      for(int i = 0; i<nombresCompletos.size(); i++){
        System.out.println(nombresCompletos.get(i));
      }
      */
      AutoCompleteDecorator.decorate(txt_alumno, nombresCompletos, false);
      
      List<String> modulos = moduloController.listarNombres();
      AutoCompleteDecorator.decorate(txt_modulos, modulos, false);
      
      List<String> detallesSemestre = semestreController.listarDetalle();
      AutoCompleteDecorator.decorate(txt_semestre, detallesSemestre, false);
    }
  
  
