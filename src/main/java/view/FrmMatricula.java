  private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String dniAlumno = txt_dni.getText();
    String idModulo = txt_moduloId.getText();
    String idSemestre = txt_semestreId.getText();
    String nota = txt_nota.getText();
    
    Matricula matri = new Matricula();
    matri.setDni(dniAlumno);
    matri.setIdModulo(idModulo);
    matri.setIdSemestre(idSemestre);
    matri.setNota(Float.parseFloat(nota));
    
    this.matriculaController.insertarAlumnosMatricula(matri);
    System.out.println("datos almacenados correctamente");
    
    this.limpiarTabla();
    this.listarBaseDeDatos();
  }//GEN-LAST:event_jButton1ActionPerformed

